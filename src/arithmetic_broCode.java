public class arithmetic_broCode {
    public static void main(String [] args){

        // Increment and Decrement Operators
        // These are shortcuts to add or subtract 1 from a variable

        int x = 1;

        // DECREMENT OPERATOR: x-- decreases x by 1
        // This is equivalent to: x = x - 1;
        x--;  // x is now 0
        System.out.println(x);  // Output: 0

        // INCREMENT OPERATOR: x++ increases x by 1
        // This is equivalent to: x = x + 1;
        x++;  // x goes from 0 to 1
        System.out.println(x);  // Output: 1


        System.out.println(x);
    }
}
