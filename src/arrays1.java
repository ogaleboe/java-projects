public class arrays1 {
    public static void main(String[] args) {

        int[] marks = {95, 72, 65, 87, 73, 20};

        //System.out.println(marks.length); //To find out how many elements an array has, use length property

        for (int count = 0; count <= 5; count++) {
            System.out.println(marks[count] + " ");
        }
    }
}
